package net.strangled.maladan;

public class CouchSize {
	private int width;
	private int length;
	public CouchSize(int width, int length) {
		super();
		this.width = width;
		this.length = length;
	}
	public int getWidth() {
		return width;
	}
	public int getLength() {
		return length;
	}

}
